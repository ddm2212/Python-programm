 # If-elif -Else statement
marks=eval(input("Enter total Marks "))
if marks>=80:
    print("First Class marks: ",marks)
elif marks>=70:
    print("Second Class, Marks: ",marks)
elif marks>=60:
    print("Third Class, Marks: ",marks)
elif marks>=50:
    print("Passed , Marks: ",marks)
else:
    print("Failed , Marks: ",marks)
